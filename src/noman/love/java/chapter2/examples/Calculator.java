package noman.love.java.chapter2.examples;

public class Calculator {

    public static void main(String[] args) {
        System.out.print("2 + 3 = ");
        System.out.println(2 + 3);
        System.out.println("2 + 3 = " + (2 + 3));
        System.out.println("2 - 3 = " + (2 - 3));
        System.out.println("2 * 3 = " + (2 * 3));
        System.out.println("2 / 3 = " + (2 / 3));
    }
}
